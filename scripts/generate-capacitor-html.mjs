/**
 * generate-capacitor-html.mjs
 *
 * After `npm run build` (TanStack Start SSR build), the dist/client/ directory
 * contains bundled JS/CSS but NO index.html (HTML is generated server-side at runtime).
 *
 * Capacitor needs dist/client/index.html as the entry point.
 * This script reads the Vite manifest and generates a minimal, valid index.html
 * that loads the TanStack Start client bundle — enabling client-side rendering on Android.
 */

import fs from 'fs';
import path from 'path';

const clientDir = 'dist/client';
const assetsDir = path.join(clientDir, 'assets');
const manifestPath = path.join(clientDir, '.vite', 'manifest.json');

// ── 1. Read Vite manifest if available ─────────────────────────────────────
let mainJs = null;
let mainCss = null;

if (fs.existsSync(manifestPath)) {
  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  const entries = Object.values(manifest);

  // Entry file for the client bundle
  const entry = entries.find(e => e.isEntry && e.file?.endsWith('.js'));
  if (entry) {
    mainJs = entry.file; // e.g. "assets/index-CEUA8kzH.js"
    mainCss = (entry.css || [])[0]; // e.g. "assets/styles-DSvFZVPS.css"
  }

  // Fallback: find CSS from all entries
  if (!mainCss) {
    const cssEntry = entries.find(e => e.file?.endsWith('.css'));
    if (cssEntry) mainCss = cssEntry.file;
  }
  console.log('✅ Read Vite manifest:', manifestPath);
}

// ── 2. Fallback: scan assets directory directly ─────────────────────────────
if (!mainJs || !mainCss) {
  if (!fs.existsSync(assetsDir)) {
    console.error('❌ Assets directory not found:', assetsDir);
    process.exit(1);
  }

  const files = fs.readdirSync(assetsDir);

  if (!mainCss) {
    const cssFile = files.find(f => f.startsWith('styles-') && f.endsWith('.css'))
                 || files.find(f => f.endsWith('.css'));
    if (cssFile) mainCss = `assets/${cssFile}`;
  }

  if (!mainJs) {
    // Find largest index-*.js (that's the main TanStack Start client bundle)
    const jsCandidates = files
      .filter(f => f.startsWith('index-') && f.endsWith('.js'))
      .map(f => ({ name: f, size: fs.statSync(path.join(assetsDir, f)).size }))
      .sort((a, b) => b.size - a.size);

    if (jsCandidates[0]) mainJs = `assets/${jsCandidates[0].name}`;
  }
  console.log('⚠️  Manifest not found, scanned assets directory instead.');
}

if (!mainJs) {
  console.error('❌ Could not locate main JS bundle in dist/client/assets/');
  process.exit(1);
}

console.log('   JS entry  :', mainJs);
console.log('   CSS entry :', mainCss || '(none found)');

// ── 3. Generate index.html ──────────────────────────────────────────────────
const cssLink = mainCss
  ? `\n  <link rel="stylesheet" crossorigin href="/${mainCss}">`
  : '';

const html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8" />
  <meta name="viewport" content="width=device-width, initial-scale=1.0, viewport-fit=cover, user-scalable=no" />
  <meta name="theme-color" content="#1a56db" />
  <title>ACCA FR Mastery</title>
  <meta name="description" content="Master ACCA Financial Reporting with confidence." />${cssLink}
</head>
<body>
  <!--
    Capacitor entry point — generated by scripts/generate-capacitor-html.mjs.
    TanStack Start client bundle renders the full React app client-side.
  -->
  <div id="root"></div>
  <script type="module" crossorigin src="/${mainJs}"></script>
</body>
</html>
`;

fs.writeFileSync(path.join(clientDir, 'index.html'), html);
console.log('✅ Generated dist/client/index.html — Capacitor is ready.');
